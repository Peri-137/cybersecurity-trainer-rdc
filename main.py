def load_text_file(file_name):
    import os
    base_path = os.path.dirname(__file__) if "__file__" in locals() else os.getcwd()
    full_path = os.path.join(base_path, "theory", file_name)

    if os.path.exists(full_path):
        with open(full_path, "r", encoding="utf-8") as f:
            return f.read()
    else:
        # Если файла нет, мы сразу увидим это на экране в виде текста
        return f"### ⚠️ Файл не найден\nНе удалось найти файл `{file_name}` в папке `theory/`. Проверьте путь!"
